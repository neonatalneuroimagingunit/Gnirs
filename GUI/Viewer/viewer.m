% Set font
defaultFontName = GHandle.Preference.Font.name;

% UI objects
GHandle.Viewer.mainFigure = figure('Position', figurePosition, ...
    'Visible', 'on', ...
    'Resize', 'on',...
    'Name', 'Viewer', ...
    'Numbertitle', 'off', ...
    'Tag', 'sensors_viewer', ...
    'Color', backgroundColor, ...
    'Toolbar', 'none', ...
    'Menubar', 'none', ...
    'DoubleBuffer', 'on', ...
    'DockControls', 'off', ...
    'Renderer', 'OpenGL');

%Hmain.mainToolbar = uitoolbar('Parent', Hmain.mainFigure);

GHandle.Viewer.menu.menuFile = uimenu('Parent', GHandle.Viewer.mainFigure, 'Label', 'File');
GHandle.Viewer.menu.menuFile_loadData = uimenu('Parent', GHandle.Viewer.menu.menuFile, ...
    'Label', 'Load Data...', ...
    'Callback', @load_data);
GHandle.Viewer.menu.menuFile_export = uimenu('Parent', GHandle.Viewer.menu.menuFile, ...
    'Label', 'Export');
GHandle.Viewer.menu.menuFile_export_export2png = uimenu('Parent', GHandle.Viewer.menu.menuFile, ...
    'Label', 'Export to png', ...
    'Callback', @export2png);
GHandle.Viewer.menu.menuFile_quit = uimenu('Parent', GHandle.Viewer.menu.menuFile, ...
    'Label', 'Quit', ...
    'Callback', {@closewindow, GHandle.Viewer.mainFigure});

GHandle.Viewer.menu.menuEdit = uimenu('Parent', GHandle.Viewer.mainFigure, 'Label', 'Edit');

GHandle.Viewer.menu.menuView = uimenu('Parent', GHandle.Viewer.mainFigure, 'Label', 'View');
GHandle.Viewer.menu.menuView_openTable = uimenu('Parent', GHandle.Viewer.menu.menuView, ...
    'Label', 'Open Table...');



%% Panels
GHandle.Temp.panellabels = {'Measure', 'Subject', 'Probe', 'Channel'};

GHandle.Temp.labels = {...
          {'Measure ID', 'Duration', 'Samples', 'Sampling Rate', 'Type', 'sciao'}, ...
          {'pippo', 'pluto'}, ...
          {'topolino', 'paperino', 'qui', 'quo', 'qua'}, ...
          {'archimede'}, ...
         };

	 
GHandle.Temp.values = {...
          {GHandle.CurrentDataSet.Measure.id, [num2str(GHandle.CurrentDataSet.Measure.timeLength) ' s'], num2str(height(GHandle.CurrentDataSet.Data)), [num2str(GHandle.CurrentDataSet.Measure.updateRate) ' Hz'], 'ciao', 'sciao'}, ...
          {'pippo', 'pluto'}, ...
          {'topolino', 'paperino', 'qui', 'quo', 'qua'}, ...
          {'archimede'}, ...
         }; 
	 
populatepanel(GHandle)

%% Panel axes
panelaxes_position = [stepW panelH+2*stepH panelaxesW panelaxesH];
GHandle.Viewer.plot.panel = uipanel('parent', GHandle.Viewer.mainFigure,...
    'Position', panelaxes_position, ...
    'Units', 'normalized', ...
    'BackgroundColor', backgroundColor, ...
    'ForegroundColor', foregroundColor, ...
    'HighlightColor', highlightColor, ...
    'ShadowColor', [211 211 211]/255, ...
    'BorderType', 'line', ...
    'FontWeight', 'bold', ...
    'FontSize', 6, ...
    'FontName', defaultFontName, ...
    'Visible', 'on', ...
    'Title', 'Time series');

GHandle.Viewer.plot.bigaxes1 = axes('parent', GHandle.Viewer.plot.panel,...
    'Position', [0.05 0.2 0.9 0.7], ...
    'Units', 'normalized', ...
    'Color', backgroundColor, ...
    'XColor', foregroundColor, ...
    'YColor', foregroundColor, ...
    'Xgrid', 'on', ...
    'Ygrid', 'on', ...
    'GridColor', foregroundColor, ...
    'FontWeight', 'bold', ...
    'FontSize', 8, ...
    'FontName', defaultFontName, ...
    'Visible', 'on', ...
    'Title', 'Main plots');

GHandle.Viewer.plot.bigaxes2 = axes('parent', GHandle.Viewer.plot.panel,...
    'Position', [0.05 0.2 0.9 0.7], ...
    'Units', 'normalized', ...
    'Color', backgroundColor, ...
    'XColor', foregroundColor, ...
    'YColor', foregroundColor, ...
    'Xgrid', 'off', ...
    'Ygrid', 'off', ...
    'XAxisLocation','top',...
    'YAxisLocation','right',...
    'GridColor', foregroundColor, ...
    'FontWeight', 'bold', ...
    'FontSize', 8, ...
    'FontName', defaultFontName, ...
    'Visible', 'on', ...
    'Title', 'Main plots');

GHandle.Viewer.plot.smallaxes = axes('parent', GHandle.Viewer.plot.panel,...
    'Units', 'normalized', ...
    'Position', [0.1 0.04 0.8 0.07], ...
    'Color', backgroundColor, ...
    'Color', backgroundColor, ...
    'XColor', foregroundColor, ...
    'YColor', foregroundColor, ...
    'GridColor', foregroundColor, ...
    'FontWeight', 'bold', ...
    'FontSize', 8, ...
    'FontName', defaultFontName, ...   
    'Visible', 'off', ...
    'Box', 'on', ...
    'XLimMode', 'auto', ...
    'YLimMode', 'auto', ...
    'XGrid', 'off', ...
    'YGrid', 'off');

GHandle.Viewer.plot.editmin = uicontrol('Parent', GHandle.Viewer.plot.panel, ...
    'Style', 'edit', ...
    'BackgroundColor', backgroundColor, ...
    'ForegroundColor', foregroundColor, ...
    'Units', 'normalized', ...
    'Position', [0.05 0.06 0.04 0.05], ...
    'FontWeight', 'bold', ...
    'FontAngle', 'italic', ...
    'Callback', {@set_time_window_min, GHandle}, ...
    'String', 'min');

GHandle.Viewer.plot.editmax = uicontrol('Parent', GHandle.Viewer.plot.panel, ...
    'Style', 'edit', ...
    'BackgroundColor', backgroundColor, ...
    'ForegroundColor', foregroundColor, ...
    'Units', 'normalized', ...
    'Position', [0.91 0.06 0.04 0.05], ...
    'FontWeight', 'bold', ...
    'FontAngle', 'italic', ...
    'Callback', {@set_time_window_max, GHandle}, ...
    'String', 'max');

GHandle.Viewer.plot.buttonBw10 = uicontrol('Parent', GHandle.Viewer.plot.panel, ...
    'Style', 'pushbutton', ...
    'BackgroundColor', backgroundColor, ...
    'ForegroundColor', foregroundColor, ...
    'Units', 'normalized', ...
    'Position', [0.05 0.03 0.02 0.03], ...
    'FontWeight', 'bold', ...
    'FontAngle', 'italic', ...
    'Callback', {@move_window, GHandle}, ...
    'String', '<<');
GHandle.Viewer.plot.buttonBw1 = uicontrol('Parent', GHandle.Viewer.plot.panel, ...
    'Style', 'pushbutton', ...
    'BackgroundColor', backgroundColor, ...
    'ForegroundColor', foregroundColor, ...
    'Units', 'normalized', ...
    'Position', [0.07 0.03 0.02 0.03], ...
    'FontWeight', 'bold', ...
    'FontAngle', 'italic', ...
    'Callback', {@move_window, GHandle}, ...
    'String', '<');
GHandle.Viewer.plot.buttonFw10 = uicontrol('Parent', GHandle.Viewer.plot.panel, ...
    'Style', 'pushbutton', ...
    'BackgroundColor', backgroundColor, ...
    'ForegroundColor', foregroundColor, ...
    'Units', 'normalized', ...
    'Position', [0.93 0.03 0.02 0.03], ...
    'FontWeight', 'bold', ...
    'FontAngle', 'italic', ...
    'Callback', {@move_window, GHandle}, ...
    'String', '>>');
GHandle.Viewer.plot.buttonFw1 = uicontrol('Parent', GHandle.Viewer.plot.panel, ...
    'Style', 'pushbutton', ...
    'BackgroundColor', backgroundColor, ...
    'ForegroundColor', foregroundColor, ...
    'Units', 'normalized', ...
    'Position', [0.91 0.03 0.02 0.03], ...
    'FontWeight', 'bold', ...
    'FontAngle', 'italic', ...
    'Callback', {@move_window, GHandle}, ...
    'String', '>');


%% Main plot
nirsdata = table2array(DataNIRS.Data(:,6:197));
channelColors = lines(size(nirsdata, 2));
aa = 1;
axes(GHandle.Viewer.plot.bigaxes1)
axis tight
cla;
hold on
for ii = 1:1:size(nirsdata,2)
    GHandle.Viewer.plot.lines1(ii) = plot(DataNIRS.Data.reltime, nirsdata(:,ii), 'LineWidth', 1, 'LineStyle', '-');
    set(GHandle.Viewer.plot.lines1(ii), 'Color', channelColors(ii,:));
    aa = aa+1;
end
GHandle.Viewer.plot.lines1_selection = findall(GHandle.Viewer.plot.lines1, 'Type', 'Line');
set(GHandle.Viewer.plot.lines1_selection, 'buttonDownFcn', {@change_line_width, channelColors, get(GHandle.Viewer.plot.lines1_selection(1), 'LineWidth'), 2})
GHandle.Viewer.plot.bigaxes1.XLabel.String = 'Time (s)';
GHandle.Viewer.plot.bigaxes1.YLabel.String = 'Intensity (a.u.)';
x2 = 1:1:size(nirsdata,1);
%y2 = 1:1:size(nirsdata,1);
y2 = DataNIRS.Data.reltime/DataNIRS.MeasureInfo.AqInfo.UpdateRate;
line(x2, y2, 'Parent', GHandle.Viewer.plot.bigaxes2, 'Color', 'none')
%linkaxes([GHandle.Viewer.plot.bigaxes1, GHandle.Viewer.plot.bigaxes2], 'x')
GHandle.Viewer.plot.bigaxes2.XLabel.String = 'Time (samples)';
GHandle.Viewer.plot.bigaxes2.YTickLabel = [];

%% Overall plot
axes(GHandle.Viewer.plot.smallaxes)
axis tight
cla;
hold on
for ii = 1:1:size(nirsdata,2)
    GHandle.Viewer.plot.lines2(ii) = plot(DataNIRS.Data.reltime, nirsdata(:,ii), 'LineWidth', 1, 'LineStyle', '-');
    set(GHandle.Viewer.plot.lines2(ii), 'Color', channelColors(ii,:));
    aa = aa+1;
end
GHandle.Viewer.plot.editmin.String = num2str(DataNIRS.Data.reltime(1));
GHandle.Viewer.plot.editmax.String = num2str(DataNIRS.Data.reltime(end));
GHandle.Viewer.plot.rectangle = rectangle('Position', [str2double(GHandle.Viewer.plot.editmin.String) min(nirsdata(:)) str2double(GHandle.Viewer.plot.editmax.String)-str2double(GHandle.Viewer.plot.editmin.String) max(nirsdata(:))-min(nirsdata(:))], 'Curvature', [0 0], 'EdgeColor', foregroundColor);
GHandle.Viewer.plot.lines2_selection = findall(GHandle.Viewer.plot.lines2, 'Type', 'Line');
set(GHandle.Viewer.plot.lines2_selection, 'buttonDownFcn', {@move_rectangle, GHandle.Viewer.plot, DataNIRS.MeasureInfo.AqInfo.UpdateRate});

%% Populate metadata

%% Turn figure on
GHandle.Viewer.mainFigure.Visible = 'on';
