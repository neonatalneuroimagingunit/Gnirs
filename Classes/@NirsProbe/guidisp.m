function  Handle = guidisp(Subject, PannelHandle , editableField)
	dxsxEdge = 0.05;
	overunderEdge = 0.009;
	textBoxWidth = 0.3;
	textBoxHeight = 0.12;
	

	% create the figure and the editable field if is not specify
	if ~exist('editableField','var')
		editableField = '';
	end
	editableField = lower(editableField);
	
	if ~exist('PannelHandle','var') || isempty(PannelHandle)
		Handle.MainFigure = figure;
		Handle.Pannel = uipanel('Parent',Handle.MainFigure);
	end
	
	% assign the title
	Handle.Pannel.Title = 'Subject';
	
	% id
	lowerPos = (0.3 +overunderEdge) + 3 *( 0.7 - overunderEdge)/4;
	Handle.IdTextBox = gtextbox(...      
		'Parent',Handle.Pannel,...
		'TextString',Subject.id,...
		'TitleString','ID:',...
		'Units','normalized',...
		'Position',[dxsxEdge lowerPos textBoxWidth textBoxHeight]);

	
	lowerPos = (0.3 +overunderEdge) + 2 *( 0.7 - overunderEdge)/4;
	% name	
	if any(contains(editableField,'name'))
		Handle.NameEditableTextBox = geditabletext(...      
		'Parent',Handle.Pannel,...
		'TextString',Subject.name,...
		'TitleString','Name',...
		'Units','normalized',...
		'Position',[dxsxEdge lowerPos textBoxWidth textBoxHeight]);
	else
		Handle.NameTextBox = gtextbox(...      
		'Parent',Handle.Pannel,...
		'TextString',Subject.name,...
		'TitleString','Name:',...
		'Units','normalized',...
		'Position',[dxsxEdge lowerPos textBoxWidth textBoxHeight]);
	end
	
	
	% Surname	
	lowerPos = (0.3 +overunderEdge) + 1 *( 0.7 - overunderEdge)/4;
	if any(contains(editableField,'surname'))
		Handle.SurnameEditableTextBox = geditabletext(...      
		'Parent',Handle.Pannel,...
		'TextString',Subject.surName,...
		'TitleString','Surname',...
		'Units','normalized',...
		'Position',[dxsxEdge lowerPos textBoxWidth textBoxHeight]);
	else
		Handle.SurnameTextBox = gtextbox(...      
		'Parent',Handle.Pannel,...
		'TextString',Subject.surName,...
		'TitleString','Surname:',...
		'Units','normalized',...
		'Position',[dxsxEdge lowerPos textBoxWidth textBoxHeight]);
	end
	
	% Birthdate
	lowerPos = (0.3 +overunderEdge);
	if any(contains(editableField,'birthdate'))
		Handle.BirthdateEditableTextBox = geditabletext(...      
		'Parent',Handle.Pannel,...
		'TextString',datestr(Subject.birthDay, 'dd/mm/yyyy'),...
		'TitleString','Name',...
		'Units','normalized',...
		'Position',[dxsxEdge lowerPos textBoxWidth textBoxHeight]);
	else
		Handle.BirthdateTextBox = gtextbox(...      
		'Parent',Handle.Pannel,...
		'TextString',datestr(Subject.birthDay, 'dd/mm/yyyy'),...
		'TitleString','Birthday:',...
		'Units','normalized',...
		'Position',[dxsxEdge lowerPos textBoxWidth textBoxHeight]);
	end
	
	% note
	if any(contains(editableField,'note'))
		Handle.NoteEditableTextBox = geditabletext(...      
		'Parent',Handle.Pannel,...
		'TextString',Subject.note,...
		'TitleString','Note',...
		'Units','normalized',...
		'Position',[dxsxEdge overunderEdge textBoxWidth 0.3-overunderEdge]);
	else
		Handle.NoteTextBox = gtextbox(...      
		'Parent',Handle.Pannel,...
		'TextString',Subject.note,...
		'TitleString','Note:',...
		'Units','normalized',...
		'Position',[dxsxEdge overunderEdge textBoxWidth 0.3-overunderEdge]);
	end
	% plot the info list
	infoList = fieldnames(Subject.Info);
	nInfo = length(infoList);
	for iInfo = 1 : nInfo
		field = infoList{iInfo};
		fieldTextString = num2str(Subject.Info.(field));
		
		ypos = (1 - overunderEdge - textBoxHeight ) -  (iInfo-1)/nInfo;
		xpos = 0.5;
			
		tag = [field ,'EditableTextBox'];
		if any(contains(editableField,field))
			Handle.(tag) = geditabletext(...      
			'Parent',Handle.Pannel,...
			'TextString',fieldTextString,...
			'TitleString',[field,':'],...
			'Units','normalized',...
			'Position',[xpos ypos textBoxWidth, textBoxHeight]);
		else
			Handle.(tag) = gtextbox(...      
			'Parent',Handle.Pannel,...
			'TextString',fieldTextString,...
			'TitleString',[field,':'],...
			'Units','normalized',...
			'Position',[xpos ypos textBoxWidth, textBoxHeight]);
		end
	end
	
end

